a=1
b=3



print(a&b)              	#按位与，输出：True
print(a|b)              	#按位或，输出：False
print(a^b)              	#按位异或，输出：True
print(~a)              	#按位取反，输出：False
print(a<<b)             	#按位左移，输出：True
print(a>>b)             	#按位右移，输出：True